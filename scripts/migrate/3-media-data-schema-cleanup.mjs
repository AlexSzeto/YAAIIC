/**
 * Migration: 3-media-data-schema-cleanup.mjs
 *
 * Cleans up media-data.json to match the new formal schema:
 *  - All workflow-specific fields are moved into a nested `extraInputs` object.
 *  - Known working-data fields are stripped entirely.
 *  - Missing core fields are filled with schema defaults.
 *  - A timestamped backup is created before any changes are written.
 *
 * Run with: node scripts/migrate/3-media-data-schema-cleanup.mjs
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

// ---------------------------------------------------------------------------
// Paths
// ---------------------------------------------------------------------------
const PROJECT_ROOT = path.resolve(__dirname, '..', '..');
const MEDIA_DATA_PATH = path.join(PROJECT_ROOT, 'server', 'database', 'media-data.json');
const SCHEMA_PATH = path.join(PROJECT_ROOT, 'server', 'resource', 'media-data-schema.json');
const WORKFLOWS_PATH = path.join(PROJECT_ROOT, 'server', 'resource', 'comfyui-workflows.json');
const BACKUP_DIR = path.join(__dirname, 'backups');

// ---------------------------------------------------------------------------
// Fields to strip entirely (generation working-data that should never persist)
// ---------------------------------------------------------------------------
const STRIP_FIELDS = new Set([
  'saveImagePath',
  'saveAudioPath',
  'saveImageFilename',
  'storagePath',
  'imagePath',
  'maskPath',
  'ollamaAPIPath',
]);

// ---------------------------------------------------------------------------
// Backup
// ---------------------------------------------------------------------------
if (!fs.existsSync(BACKUP_DIR)) {
  fs.mkdirSync(BACKUP_DIR, { recursive: true });
}
const timestamp = new Date().toISOString().replace(/[:.]/g, '-');
const backupPath = path.join(BACKUP_DIR, `media-data-backup-${timestamp}.json`);
fs.copyFileSync(MEDIA_DATA_PATH, backupPath);
console.log(`Backup created at: ${backupPath}`);

// ---------------------------------------------------------------------------
// Load data
// ---------------------------------------------------------------------------
const schema = JSON.parse(fs.readFileSync(SCHEMA_PATH, 'utf8'));
const coreFields = new Set(Object.keys(schema.fields));

const workflowsData = JSON.parse(fs.readFileSync(WORKFLOWS_PATH, 'utf8'));
const workflowMap = new Map((workflowsData.workflows || []).map(wf => [wf.name, wf]));

const db = JSON.parse(fs.readFileSync(MEDIA_DATA_PATH, 'utf8'));

// ---------------------------------------------------------------------------
// Helper: build the set of valid extra-input IDs for a workflow
// ---------------------------------------------------------------------------
function buildValidExtraInputIds(wf) {
  const ids = new Set();
  const opts = wf.options || {};
  if (Array.isArray(opts.extraInputs)) {
    opts.extraInputs.forEach(input => ids.add(input.id));
  }
  const inputImages = opts.inputImages || 0;
  for (let i = 0; i < inputImages; i++) ids.add(`image_${i}_uid`);
  const inputAudios = opts.inputAudios || 0;
  for (let i = 0; i < inputAudios; i++) ids.add(`audio_${i}_uid`);
  return ids;
}

// ---------------------------------------------------------------------------
// Process each entry
// ---------------------------------------------------------------------------
let knownWorkflowCount = 0;
let unknownWorkflowCount = 0;
let totalFieldsRemoved = 0;

db.mediaData = db.mediaData.map(entry => {
  const wf = workflowMap.get(entry.workflow) || null;
  if (wf) {
    knownWorkflowCount++;
  } else {
    unknownWorkflowCount++;
  }

  // Collect existing extraInputs
  const existingExtras = (entry.extraInputs && typeof entry.extraInputs === 'object' && !Array.isArray(entry.extraInputs))
    ? { ...entry.extraInputs }
    : {};

  const cleanedEntry = {};

  // Categorize all top-level fields
  for (const [key, value] of Object.entries(entry)) {
    if (key === 'extraInputs') continue; // handled separately

    if (STRIP_FIELDS.has(key)) {
      // Strip entirely
      totalFieldsRemoved++;
    } else if (coreFields.has(key)) {
      cleanedEntry[key] = value;
    } else {
      // Non-core, non-strip: move into extraInputs
      existingExtras[key] = value;
      totalFieldsRemoved++;
    }
  }

  // Fill missing core fields with schema defaults (skip auto-generated)
  for (const [field, def] of Object.entries(schema.fields)) {
    if (def.autoGenerated) continue;
    if (cleanedEntry[field] === undefined) {
      cleanedEntry[field] = def.default !== undefined ? def.default : null;
    }
  }

  // Filter / fill extraInputs
  let finalExtras;
  if (wf) {
    const validIds = buildValidExtraInputIds(wf);
    finalExtras = {};

    for (const [key, value] of Object.entries(existingExtras)) {
      if (validIds.has(key)) {
        finalExtras[key] = value;
      }
    }

    // Fill missing extra input defaults from workflow config
    const opts = wf.options || {};
    if (Array.isArray(opts.extraInputs)) {
      opts.extraInputs.forEach(input => {
        if (finalExtras[input.id] === undefined && input.default !== undefined) {
          finalExtras[input.id] = input.default;
        }
      });
    }
  } else {
    // Unknown workflow: preserve all non-core fields in extraInputs without filtering
    finalExtras = existingExtras;
  }

  cleanedEntry.extraInputs = finalExtras;
  return cleanedEntry;
});

// ---------------------------------------------------------------------------
// Write back
// ---------------------------------------------------------------------------
fs.writeFileSync(MEDIA_DATA_PATH, JSON.stringify(db, null, 2), 'utf8');

console.log('\n--- Migration Summary ---');
console.log(`Total entries processed:         ${db.mediaData.length}`);
console.log(`Entries with known workflow:     ${knownWorkflowCount}`);
console.log(`Entries with unknown workflow:   ${unknownWorkflowCount}`);
console.log(`Top-level fields removed/moved:  ${totalFieldsRemoved}`);
console.log('Done.');
